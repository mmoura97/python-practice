# Exercicio - separador_numero

numero = (input('Entre com um numero:'))
numero1 = numero [0]
numero2 = numero [1]
numero3 = numero [2]

centena = int (numero1) * 100
dezena = int (numero2) * 10
unidade = int (numero3) * 1

print(f'O numero escolhido é {numero}')
print(f'A centena do numero escolhido é {centena}')
print(f'A dezena do numero escolhido é {dezena}')
print(f'A unidade do numero escolhido é {unidade}')


